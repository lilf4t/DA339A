

import java.util.Scanner;


public class Uppgift1a{
  public static void main(String[] args) {

//for loop 1
  for (int i = 1; i <= 10; i++)
  {
    System.out.println(i);
  }


// for loop 2
 for (int i = 10; i >= 1; i--) {
   System.out.println(i);
 }

//for loop 3
 for (int i = 2; i <= 10; i = i + 2)
  {
   System.out.println(i);
  }

//for loop 4
 for (int i = 9; i >= 1; i = i - 2)
  {
   System.out.println(i);
  }

//for loop 5
for (int i = 10; i <= 100; i = i + 10)
  {
   System.out.println(i);
  }

  }
}
